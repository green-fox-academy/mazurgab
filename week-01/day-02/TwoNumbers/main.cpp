#include <iostream>

int main(int argc, char* args[]) {

    int a = 22;
    int b = 13;

    std::cout << a + b << std::endl;
    std::cout << a - b << std::endl;
    std::cout << a * b << std::endl;
    std::cout << (float)a / b << std::endl;
    std::cout << a / b << std::endl;
    std::cout << a % b << std::endl;

    return 0;
}